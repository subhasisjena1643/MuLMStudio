@app.get("/demo")
async def demo(view: str = "transformer") -> JSONResponse:
    """
    Pre-computed static demo graph for VITE_DEMO_MODE=true.

    Query params:
      ?view=transformer      — full TransformerEncoderBlock graph (default)
      ?view=mha_interior     — MHA drill-down interior view

    The static_demo_graph.json is generated by running:
        python serializer.py   (which also saves static_demo_graph.json)
    If the file doesn't exist, this endpoint returns a live-computed result.
    """
    if view == "mha_interior":
        return JSONResponse(build_mha_interior_view(embed_dim=512, num_heads=8))

    if _STATIC_DEMO_PATH.exists():
        with open(_STATIC_DEMO_PATH, encoding="utf-8") as f:
            static = json.load(f)
        # static_demo_graph.json has key "transformer" (saved by serializer.py test)
        graph = static.get("transformer") or static
        return JSONResponse(graph)

    # Fallback: compute live if file not found
    _TRANSFORMER_CODE = """
import torch
import torch.nn as nn

class TransformerEncoderBlock(nn.Module):
    def __init__(self, d_model=512, nhead=8, dim_feedforward=2048, dropout=0.1):
        super().__init__()
        self.attention   = nn.MultiheadAttention(d_model, nhead, dropout=dropout, batch_first=True)
        self.norm1       = nn.LayerNorm(d_model)
        self.norm2       = nn.LayerNorm(d_model)
        self.feedforward = nn.Sequential(
            nn.Linear(d_model, dim_feedforward),
            nn.ReLU(),
            nn.Dropout(dropout),
            nn.Linear(dim_feedforward, d_model),
        )
        self.dropout = nn.Dropout(dropout)

    def forward(self, x):
        attn_out, _ = self.attention(x, x, x)
        x = self.norm1(x + self.dropout(attn_out))
        ff_out = self.feedforward(x)
        x = self.norm2(x + self.dropout(ff_out))
        return x
"""
    loop = asyncio.get_event_loop()
    result = await loop.run_in_executor(
        _executor, _do_trace, _TRANSFORMER_CODE, [2, 128, 512], "float32"
    )
    if result["status"] == "success":
        return JSONResponse(result["graph"])
    return JSONResponse(result, status_code=500)


