class MismatchRequest(BaseModel):
    """
    Request body for POST /mismatches.
    graph — the current graph_to_json() output (nodes + edges).
    """
    graph: Dict[str, Any]


@app.post("/mismatches")
async def check_mismatches(req: MismatchRequest) -> JSONResponse:
    """
    Run shape-mismatch detection on an arbitrary graph JSON.

    Accepts the same graph schema emitted by the WebSocket /ws/trace endpoint.
    Returns:
      { "mismatches": [ { edge_id, source_id, target_id, message, severity } ] }

    The WebSocket endpoint already includes mismatches inline in every success
    response. This REST endpoint exists for:
      - Testing / debugging during development
      - VITE_DEMO_MODE (frontend fetches /demo then calls /mismatches to annotate)
      - Future: re-running detection after the user edits the graph on the canvas
    """
    try:
        results = detect_mismatches(req.graph)
    except Exception as exc:
        return JSONResponse(
            {"error": str(exc), "mismatches": []},
            status_code=500,
        )
    return JSONResponse({"mismatches": results})


@app.post("/export")
async def export_model(req: ExportRequest) -> Response:
    """
    Generate a downloadable standalone .py file from the current canvas state.

    The exported file is a clean, self-contained Python script that:
      - Defines the model class exactly as typed in the notebook
      - Includes a __main__ block with a forward-pass smoke test
      - Has a µLM Studio header comment with shape + architecture metadata

    Request body: { "code": str, "graph": {...}, "model_name": str | null }
    Response:     text/plain with Content-Disposition: attachment
    """
    # Determine model name: prefer explicit, else scan graph nodes
    model_name = req.model_name
    if not model_name:
        # Try to read it from the graph — the canvas always has the last good name
        nodes = req.graph.get("nodes", [])
        # Use the first call_module node's label as the architecture name
        module_node = next(
            (n for n in nodes if n.get("data", {}).get("op") == "call_module"),
            None,
        )
        model_name = module_node["data"].get("label", "Model") if module_node else "Model"

    try:
        py_source = _generate_export_code(
            notebook_code=req.code,
            graph=req.graph,
            model_name=model_name,
        )
    except Exception as exc:
        # Export generation failed — return the raw code with a minimal header
        # so the user is never left with nothing to download.
        now = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
        py_source = (
            f"# Generated by µLM Studio — {now}\n"
            f"# Note: automatic header generation failed ({type(exc).__name__}: {exc})\n\n"
            + req.code
        )

    safe_name = model_name.replace(" ", "_").replace("/", "_") or "model"
    filename  = f"{safe_name}.py"

    return Response(
        content=py_source,
        media_type="text/plain; charset=utf-8",
        headers={"Content-Disposition": f'attachment; filename="{filename}"'},
    )


