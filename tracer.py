class ExportRequest(BaseModel):
    """
    Request body for POST /export.

    code       — the full notebook code string (what the user typed)
    graph      — the last successful graph_to_json() output (for metadata)
    model_name — optional override; inferred from graph nodes if omitted
    """
    code:       str
    graph:      Dict[str, Any]
    model_name: Optional[str] = None


def _generate_export_code(
    notebook_code: str,
    graph: Dict[str, Any],
    model_name: str,
) -> str:
    """
    Produce a clean, standalone .py file from the notebook code + graph metadata.

    Output layout
    -------------
    1. µLM Studio header comment (architecture, date, shape summary)
    2. Deduplicated import block (torch + nn always guaranteed)
    3. The model class source (from notebook_code, stripped of stray lines)
    4. A runnable __main__ block with a forward-pass smoke test

    The function is defensive: if any inspection step fails it falls back
    to the raw notebook code with just the header prepended.
    """
    # ── Collect shape metadata from graph for the header ─────────────────────
    nodes = graph.get("nodes", [])
    input_node  = next((n for n in nodes if n.get("type") == "mlmInputNode"),  None)
    output_node = next((n for n in nodes if n.get("type") == "mlmOutputNode"), None)
    input_shape  = input_node["data"]["shape"]  if input_node  else "unknown"
    output_shape = output_node["data"]["shape"] if output_node else "unknown"

    # Count by category for the architecture summary line
    categories: Dict[str, int] = {}
    for n in nodes:
        cat = n.get("data", {}).get("category", "CORE")
        categories[cat] = categories.get(cat, 0) + 1
    arch_summary = ", ".join(f"{v}× {k}" for k, v in sorted(categories.items()) if k not in ("INPUT", "OUTPUT"))

    # ── Build the __main__ smoke test ─────────────────────────────────────────
    # Infer a concrete dummy tensor from the input_shape string, e.g. "[2, 128, 512]"
    try:
        # Parse the shape string e.g. "[2, 128, 512]" → [2, 128, 512]
        raw = input_shape.strip("[]").split(",")
        dims = [int(d.strip()) for d in raw if d.strip().lstrip("-").isdigit()]
        if dims:
            dtype_node = input_node["data"].get("params", {}).get("dtype", "")
            if "int" in dtype_node.lower():
                dummy_call = f"torch.zeros({dims}, dtype=torch.long)"
            else:
                dummy_call = f"torch.randn({dims})"
        else:
            dummy_call = "torch.randn(2, 128, 512)  # adjust shape for your model"
    except Exception:
        dummy_call = "torch.randn(2, 128, 512)  # adjust shape for your model"

    main_block = textwrap.dedent(f"""
        if __name__ == "__main__":
            model = {model_name}()
            model.eval()
            x = {dummy_call}
            with torch.no_grad():
                out = model(x)
            print(f"Input  shape : {{x.shape}}")
            print(f"Output shape : {{out.shape}}")
            print(f"Parameters   : {{sum(p.numel() for p in model.parameters()):,}}")
    """).rstrip()

    # ── Ensure import block ───────────────────────────────────────────────────
    has_torch_import = "import torch" in notebook_code
    has_nn_import    = "import torch.nn" in notebook_code or "from torch import nn" in notebook_code

    import_lines: List[str] = []
    if not has_torch_import:
        import_lines.append("import torch")
    if not has_nn_import:
        import_lines.append("import torch.nn as nn")
    import_block = ("\n".join(import_lines) + "\n\n") if import_lines else ""

    # ── Strip the notebook code of any trailing whitespace/blank lines ────────
    clean_code = notebook_code.rstrip()

    # ── Assemble the final file ───────────────────────────────────────────────
    now = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
    header = textwrap.dedent(f"""\
        # =============================================================================
        # Generated by µLM Studio
        # Architecture : {model_name}
        # Summary      : {arch_summary}
        # Input shape  : {input_shape}
        # Output shape : {output_shape}
        # Exported     : {now}
        # =============================================================================
    """)

    return "\n".join(filter(None, [
        header,
        import_block,
        clean_code,
        "",
        main_block,
        "",  # trailing newline
    ]))

