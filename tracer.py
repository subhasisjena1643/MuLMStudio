@app.websocket("/ws/trace")
async def ws_trace(websocket: WebSocket) -> None:
    """
    Real-time notebook → canvas tracing endpoint.

    The frontend connects once and sends messages with updated code on every
    debounced keystroke (300ms debounce in useTracer.js). The connection is
    kept alive for the duration of the session.

    On each message:
      1. Parse the JSON payload.
      2. Run tracing in the thread pool (non-blocking).
      3. Return the serialized graph or a structured error.
    """
    await websocket.accept()

    try:
        while True:
            raw = await websocket.receive_text()

            # ── Parse incoming message ────────────────────────────────────────
            try:
                msg: Dict[str, Any] = json.loads(raw)
            except json.JSONDecodeError as exc:
                await websocket.send_text(json.dumps({
                    "status": "error",
                    "error": {
                        "phase":   "parse",
                        "type":    "JSONDecodeError",
                        "message": f"Invalid JSON: {exc}",
                        "hint":    "The message must be a JSON object.",
                    },
                }))
                continue

            code        = msg.get("code", "")
            input_shape = msg.get("input_shape")   # list[int] or None
            input_dtype = msg.get("input_dtype")   # str or None

            if not code.strip():
                await websocket.send_text(json.dumps({
                    "status": "error",
                    "error": {
                        "phase":   "parse",
                        "type":    "ValueError",
                        "message": "Empty code received.",
                        "hint":    "Provide a PyTorch nn.Module class definition.",
                    },
                }))
                continue

            # ── Run tracing in thread pool (non-blocking) ─────────────────────
            loop = asyncio.get_event_loop()
            try:
                result = await asyncio.wait_for(
                    loop.run_in_executor(
                        _executor,
                        _do_trace,
                        code,
                        input_shape,
                        input_dtype,
                    ),
                    timeout=30.0,   # 30-second hard timeout per trace request
                )
            except asyncio.TimeoutError:
                result = {
                    "status": "error",
                    "error": {
                        "phase":   "trace",
                        "type":    "TimeoutError",
                        "message": "Tracing timed out after 30 seconds.",
                        "hint":    "Your model may contain an infinite loop or be too large.",
                    },
                }

            await websocket.send_text(json.dumps(result))

    except WebSocketDisconnect:
        pass   # Client disconnected — normal, nothing to log
    except Exception as exc:
        # Catch-all: something unexpected happened (OOM, driver crash, etc.).
        # Try to send a final error frame before the connection closes.
        # We do NOT re-raise — uvicorn will close the socket gracefully.
        try:
            await websocket.send_text(json.dumps({
                "status": "error",
                "error": {
                    "phase":   "server",
                    "type":    type(exc).__name__,
                    "message": str(exc),
                    "hint":    "An unexpected server error occurred. Reconnect to continue.",
                    "traceback": tb.format_exc(),
                },
            }))
        except Exception:
            pass  # socket already dead — nothing we can do


