# ─────────────────────────────────────────────────────────────────────────────
# Code execution sandbox
# ─────────────────────────────────────────────────────────────────────────────

# All classes defined by torch.nn, used to distinguish user-defined modules
# from torch built-ins when scanning the exec namespace.
_TORCH_NN_CLASSES: frozenset[type] = frozenset(
    obj for obj in vars(torch.nn).values()
    if isinstance(obj, type) and issubclass(obj, nn.Module)
)

# Modules blocked from import in the exec sandbox.
# This guards against accidental misuse (e.g. someone writes `import os; os.system(...)` )
# NOT designed to stop a determined adversary — this is a local single-user tool.
_BLOCKED_IMPORTS: frozenset[str] = frozenset([
    "os", "subprocess", "socket", "shutil", "pty",
    "signal", "ctypes", "cffi", "multiprocessing",
])


def _safe_import(name: str, *args: Any, **kwargs: Any) -> Any:
    """
    Replacement for __import__ in the exec sandbox.
    Blocks a small set of modules that have no business appearing in
    a PyTorch model definition (os, subprocess, socket, etc.).
    All torch/numpy/math imports pass through normally.
    """
    if name in _BLOCKED_IMPORTS:
        raise ImportError(
            f"Import '{name}' is not allowed in the µLM Studio sandbox. "
            "If you need this for your model, remove it from the cell first."
        )
    import builtins as _builtins
    return _builtins.__import__(name, *args, **kwargs)


def _build_exec_namespace() -> Dict[str, Any]:
    """
    Build the namespace for exec()'ing user PyTorch code.

    Provides the standard imports a PyTorch user expects at module level
    without needing to write them (though writing them is also fine — exec
    will just shadow our defaults).

    Important: always pass the builtins MODULE (not the __builtins__ name).
    In non-__main__ contexts (e.g. thread pool workers) __builtins__ is a
    dict that lacks __import__, silently breaking any import statement inside
    exec'd code. `import builtins` always gives the full module.
    """
    import builtins
    import torch.nn.functional as F
    import math

    return {
        "__builtins__": builtins,    # full module — guarantees __import__ is present
        "__import__":   _safe_import, # gated importer — blocks os/subprocess/socket
        "torch":  torch,
        "nn":     nn,
        "F":      F,
        "math":   math,
        "Tensor": torch.Tensor,
    }


def _find_model_class(
    namespace: Dict[str, Any],
    base_names: frozenset[str],
) -> Tuple[str, type]:
    """
    Scan the exec namespace for an nn.Module subclass defined by the user.

    Parameters
    ----------
    namespace   — the namespace dict after exec()
    base_names  — names that existed BEFORE exec (to identify new entries)

    Returns (class_name, class_object). Raises ValueError if none found.
    """
    candidates: List[Tuple[str, type]] = []

    for name, obj in namespace.items():
        if name in base_names:
            continue  # existed before exec — not user-defined
        if name.startswith("_"):
            continue
        if (
            isinstance(obj, type)
            and issubclass(obj, nn.Module)
            and obj not in _TORCH_NN_CLASSES
        ):
            candidates.append((name, obj))

    if not candidates:
        raise ValueError(
            "No nn.Module subclass found in the provided code. "
            "Define a class that inherits from nn.Module."
        )

    # If multiple candidates, return the last one — it's typically the
    # top-level class that composes the others (e.g., TransformerEncoderBlock
    # appears after its sub-modules in source order).
    return candidates[-1]


def _dtype_from_string(dtype_str: str) -> torch.dtype:
    """Map a dtype string from the WebSocket message to a torch.dtype."""
    _MAP = {
        "float32": torch.float32,
        "float":   torch.float32,
        "float16": torch.float16,
        "int64":   torch.int64,
        "long":    torch.int64,
        "int32":   torch.int32,
        "bool":    torch.bool,
    }
    return _MAP.get(dtype_str.lower(), torch.float32)


def _infer_dummy_input(
    model: nn.Module,
    requested_shape: Optional[List[int]],
    requested_dtype: Optional[str],
) -> torch.Tensor:
    """
    Produce a dummy input tensor suitable for ShapeProp on `model`.

    Priority:
      1. Use requested_shape + requested_dtype if provided by client.
      2. Detect model structure:
         - Contains nn.Embedding → int64 [2, 128]
         - Contains nn.Conv2d   → float32 [2, C_in, 32, 32]
         - Default              → float32 [2, 128, 512]  (transformer-style)

    Returns a zero/randn tensor on CPU.
    """
    import inspect

    dtype = _dtype_from_string(requested_dtype or "float32")

    if requested_shape:
        shape = requested_shape
        if dtype == torch.int64:
            return torch.zeros(shape, dtype=torch.int64)
        return torch.randn(shape, dtype=dtype)

    # ── Auto-detect from model structure ─────────────────────────────────────
    has_embedding = any(isinstance(m, nn.Embedding) for m in model.modules())
    conv2d_modules = [m for m in model.modules() if isinstance(m, nn.Conv2d)]

    if has_embedding and not conv2d_modules:
        # Sequence model with embedding (e.g. TissueLLMCell)
        return torch.zeros(2, 128, dtype=torch.int64)

    if conv2d_modules:
        # CNN: determine C_in from the first Conv2d
        first_conv = conv2d_modules[0]
        c_in = first_conv.in_channels
        return torch.randn(2, c_in, 32, 32)

    # ── Heuristic from forward() parameter name ───────────────────────────────
    try:
        sig = inspect.signature(model.forward)
        params = [p for p in sig.parameters if p != "self"]
        if params:
            first = params[0].lower()
            if any(kw in first for kw in ("token", "idx", "id", "index")):
                return torch.zeros(2, 128, dtype=torch.int64)
    except (ValueError, TypeError):
        pass

    # Default: 3-D transformer-style input
    return torch.randn(2, 128, 512)


# ─────────────────────────────────────────────────────────────────────────────
# Core tracing logic  (synchronous — runs in thread pool)
# ─────────────────────────────────────────────────────────────────────────────

def _do_trace(
    code: str,
    input_shape: Optional[List[int]],
    input_dtype: Optional[str],
) -> Dict[str, Any]:
    """
    Execute user code, trace the model, propagate shapes, and serialize.

    This function is synchronous and CPU-bound. It must be called via
    run_in_executor to avoid blocking the asyncio event loop.

    Returns a dict ready for JSON serialisation — either:
      {"status": "success", "graph": {...}, "model_name": ..., "trace_time_ms": ...}
    or
      {"status": "error", "error": {"phase": ..., "type": ..., "message": ..., "traceback": ...}}
    """
    t_start = time.perf_counter()

    # ── Phase 1: exec user code ───────────────────────────────────────────────
    namespace = _build_exec_namespace()
    base_names = frozenset(namespace.keys())

    try:
        exec(compile(code, "<mulm_user_code>", "exec"), namespace)  # noqa: S102
    except SyntaxError as exc:
        return _error_response(
            phase="parse",
            exc=exc,
            hint="Check for syntax errors in your model definition.",
        )
    except Exception as exc:
        return _error_response(
            phase="exec",
            exc=exc,
            hint="An error occurred while executing your model code.",
        )

    # ── Phase 2: locate and instantiate model class ───────────────────────────
    try:
        model_name, model_cls = _find_model_class(namespace, base_names)
        model = model_cls()
        model.eval()
    except Exception as exc:
        return _error_response(
            phase="exec",
            exc=exc,
            hint=(
                "Could not instantiate your model. "
                "Ensure the class has default values for all __init__ parameters."
            ),
        )

    # ── Phase 3: build dummy input ────────────────────────────────────────────
    try:
        dummy = _infer_dummy_input(model, input_shape, input_dtype)
    except Exception as exc:
        return _error_response(
            phase="exec",
            exc=exc,
            hint="Could not construct a dummy input tensor. Provide input_shape explicitly.",
        )

    # ── Phase 4: symbolic_trace ───────────────────────────────────────────────
    try:
        traced = fx.symbolic_trace(model)
    except Exception as exc:
        return _error_response(
            phase="trace",
            exc=exc,
            hint=(
                "torch.fx symbolic_trace failed. "
                "If your model contains data-dependent control flow or "
                "non-traceable operations, consider wrapping them."
            ),
        )

    # ── Phase 5: ShapeProp ────────────────────────────────────────────────────
    try:
        sp = ShapeProp(traced)
        sp.propagate(dummy)
    except Exception as exc:
        # ShapeProp failure is non-fatal: we can still serialize without shapes.
        # Log the error but continue — graph_to_json handles [unknown] gracefully.
        shape_error = _error_payload(phase="shape_prop", exc=exc)
    else:
        shape_error = None

    # ── Phase 6: serialize ────────────────────────────────────────────────────
    try:
        graph_json = graph_to_json(traced, model)
    except Exception as exc:
        return _error_response(
            phase="serialize",
            exc=exc,
            hint="Serialization failed. This is an internal error — please report it.",
        )

    t_ms = (time.perf_counter() - t_start) * 1000

    # ── Phase 7: mismatch detection ───────────────────────────────────────────
    # Run detect_mismatches on the serialized graph. Any failures here are
    # non-fatal — a detection bug must never prevent the graph from rendering.
    try:
        mismatches = detect_mismatches(graph_json)
    except Exception:
        mismatches = []

    # Stamp edges whose id appears in mismatches with status="mismatch"
    mismatch_edge_ids = {m["edge_id"] for m in mismatches}
    for edge in graph_json.get("edges", []):
        if edge.get("id") in mismatch_edge_ids:
            if edge.get("data") is None:
                edge["data"] = {}
            edge["data"]["status"] = "mismatch"

    result: Dict[str, Any] = {
        "status":        "success",
        "graph":         graph_json,
        "model_name":    model_name,
        "input_shape":   list(dummy.shape),
        "input_dtype":   str(dummy.dtype).replace("torch.", ""),
        "trace_time_ms": round(t_ms, 2),
        "mismatches":    mismatches,    # list of { edge_id, message, severity }
    }

    if shape_error:
        # Partial success: tracing worked but ShapeProp degraded
        result["warnings"] = [shape_error]

    return result


def _error_payload(
    phase: str,
    exc: Exception,
    hint: str = "",
) -> Dict[str, Any]:
    """Build the error dict that goes inside the 'error' key of the response."""
    full_tb = tb.format_exception(type(exc), exc, exc.__traceback__)
    return {
        "phase":     phase,
        "type":      type(exc).__name__,
        "message":   str(exc),
        "hint":      hint,
        "traceback": "".join(full_tb),
    }


def _error_response(
    phase: str,
    exc: Exception,
    hint: str = "",
) -> Dict[str, Any]:
    """Build the top-level error response dict."""
    return {
        "status": "error",
        "error":  _error_payload(phase=phase, exc=exc, hint=hint),
    }


